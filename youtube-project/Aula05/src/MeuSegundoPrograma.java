public class MeuSegundoPrograma {
    public static void main(String[] args) {
        var initialText = "Bora testar!";
        var phrase = "Meu nome é ";
        var name = "Luiz ";
        var lastname = "Antunes";

        var newCar = "Honda Fit";   // variaveis -> tipo: Textos (Strings)
        var year = 2004;             // variaveis -> tipo: Numeros (Int)
        var km = 200.5;             // variaveis -> tipo: Numeros fracionados (Double)
        var available = true;       // variaveis -> tipo: Booleanas (Verdadeiro | Falso)

        System.out.println(initialText);
        System.out.println(phrase + name + lastname);

        System.out.println(newCar);
        System.out.println(year);
        System.out.println(km);
        System.out.println(available);
    }
}

